#include <minishell.h>

int check_upper(char *s)
{
	int i;

	i = 0;
	while (s[i])
	{
		if (s[i] >= 'a' && s[i] <= 'z')
		{
			break;
			return (0);
			
		}
		i++;
	}
	return (1);
}